package br.com.joaogcm.jg.restaurante.caseiro.model;

public class Perfil {

	Integer codigo;
	String nome;

	public Perfil() {

	}

	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}